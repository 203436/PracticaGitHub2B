public class MonElectronico {

    private String numMonedero;
    private double dineroElec;

    public void setNumMonedero(String numMonedero) {
        this.numMonedero = numMonedero;
    }

    public String getNumMonedero() {
        return numMonedero;
    }

    public void setDineroElec(double dineroElec) {
        this.dineroElec = dineroElec;
    }

    public double getDineroElec() {
        return dineroElec;
    }


}
